
	<p> <strong>How to Fill a Vacancy</strong>
	<p>  To fill a vacancy, you must have already selected <strong>(edit this week)</strong> at the top of the calendar.
	<p> <B>Step 1:</B> Now click on a calendar shift that has a vacancy, like this:<BR><BR>
	<a href="tutorial/screenshots/assigntoshiftstep1.png" class="image" title="assigntoshiftstep1.png"
			horizontalalign="center" target="tutorial/screenshots/assigntoshiftstep1.png">
		 &nbsp&nbsp&nbsp&nbsp<img src="tutorial/screenshots/assigntoshiftstep1.png" width="10%" border="1px" align="center"></a>
	<br><br> (Each upcoming calendar shift will turn gray whenever the mouse passes over it.  Shifts for prior days on the calendar cannot
	be edited in this way.)
	<p> <B>Step 2:</B> This will give you a shift form that looks like this:<BR><BR>
	<a href="tutorial/screenshots/assigntoshiftstep2.png" class="image" title="assigntoshiftstep2.png"
			horizontalalign="center" target="tutorial/screenshots/assigntoshiftstep2.png">
		 &nbsp&nbsp&nbsp&nbsp<img src="tutorial/screenshots/assigntoshiftstep2.png" width="10%" border="1px" align="center"></a>
	<p> <B>Step 3:</B> Click on the <B>Assign Volunteer</B> button in the right hand column of a <B>vacant</B> row, like this:<BR><BR>
		<a href="tutorial/screenshots/assigntoshiftstep3.png" class="image" title="assigntoshiftstep3.png"
			horizontalalign="center" target="tutorial/screenshots/assigntoshiftstep3.png">
		 &nbsp&nbsp&nbsp&nbsp<img src="tutorial/screenshots/assigntoshiftstep3.png" width="10%" border="1px" align="center"></a>
	<BR><br>&nbsp&nbsp&nbsp NOTE: If there is no <B>vacant</B> row, then that shift has no vacancies to be filled.<BR><BR>

	<p> <B>Step 4:</B> You can now view the list of volunteers who are available for that shift, like this:<BR><BR>
        <a href="tutorial/screenshots/assigntoshiftstep4.png" class="image" title="assigntoshiftstep4.png"
        	target="tutorial/screenshots/assigntoshiftstep4.png">
		 &nbsp&nbsp&nbsp&nbsp<img src="tutorial/screenshots/assigntoshiftstep4.png" width="10%" border="1px" align="middle"></a>
<br><br> (Alternatively, you can view the entire list of volunteers, whether or not they are available for that shift.)
	<p> <B>Step 5:</B> Select the volunteer you want to assign to this shift, and then click <B>Add Volunteer</B>, like this:<BR><BR>
		<a href="tutorial/screenshots/assigntoshiftstep5.png" class="image" title="assigntoshifttep5.png"
        	target="tutorial/screenshots/assigntoshiftstep5.png">
		 &nbsp&nbsp&nbsp&nbsp<img src="tutorial/screenshots/assigntoshiftstep5.png" width="10%" border="1px" align="middle"></a>
	<p> <B>Step 6:</B>  You can return to the shift form by selecting <strong>back to shift</strong> or to the calendar by
	selecting <strong>back to calendar</strong>.  You can return to any other function by selecting it on the navigation bar.