				<p> <B>Step 7:</B> Five additional <strong>Master Schedule</strong> functions are also available.  You can add or remove an entire shift, 
				    you can remove a person from a slot, or you can either add or remove a slot from the shift.
				    <ul>
				    <li>To add a new shift, select an unscheduled (dark gray) time slot on the Master Schedule and then select a new <strong>starting time</strong> and <strong>ending time</strong> on the worksheet that appears.
				    <li>To remove an existing shift from the Master Schedule, select <strong>Remove Entire Shift</strong> on the worksheet shown in <B>Step 3</B>.
				    <li>To remove a person from a shift, select
				    <strong>Remove Person/Create Vacancy</strong> on the worksheet shown in <B>Step 3</B>.
				    <li>To add a new slot to a shift, select <strong>Add Slot</strong> on the worksheet shown in <B>Step 3</B>.
				    <li>To remove a vacant slot from a shift, select <strong>Remove Vacant Slot</strong> on the worksheet shown in <B>Step 3</B>.
				    </ul>
