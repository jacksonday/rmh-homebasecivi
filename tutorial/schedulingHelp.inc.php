
				<p> <strong>How to Manage the Master Schedule</strong>
				<p>The Master Schedule has eight parts: one for the <b>Reception</b>, one for the <b>Family room</b>, and so on.  The examples below
				show how to work with the <b>Reception</b> master schedule.  These same steps can be followed when working with any other
				master schedule.

				<p> <B>Step 1:</B> On the navigation bar, select <B>master schedule: reception</B> and you will see two tables.
				The first shows the persons scheduled for the odd weeks (<strong>Group One</strong>) of the year, and the second
				     shows the persons scheduled for the even weeks (<strong>Group Two</strong>). 
				     Here is a sample <strong>Group One</strong> table with the persons already scheduled:
					<br><br><a href="tutorial/screenshots/scheduling1.png" class="image" title="addPersonStep1.png" horizontalalign="center">
					 <img src="tutorial/screenshots/scheduling1.png" width="10%" border="1px" align="middle"></a>
				<br><br> A "shift" is shown as a box associated with a particular day (e.g., Thursday) and time slot (e.g., 6-9pm).
					 If a shift has vacancies, the number of vacancies is shown in <strong>boldface</strong>.  For example, the Thursday 6-9pm
					 shift has one vacancy in the <strong>Group One</strong> schedule shown above.
				<p> <B>Step 2:</B> If you want to fill a vacancy for a shift, you must first select it.
				<br>For example, suppose you want to
				add a person to fill the vacancy on the <strong>Group One</strong> Thursday 6-9pm shift.  To do this, you
				first select that shift on the schedule.
