
<p>
	<strong>Working with the Calendar</strong>
<p>
	The calendar has eight different pages, one for each venue: the <strong>Reception</strong>
	calendar, the <strong>Family room</strong> calendar, and six others.
	Each calendar shows all the persons scheduled to volunteer for each
	shift on every day of a single week.
