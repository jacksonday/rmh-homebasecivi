
	<p> <strong>How to Add Notes</strong>
	<p> Managers can record notes on any shift that will be read by all volunteers viewing the calendar.  Managers may also
	enter a note for an entire day at the bottom of that day on the calendar.
	<p>  To begin, you must have already selected <strong>(edit this week)</strong> at the top of the calendar.
	<p> <B>Step 1:</B> At the bottom of each shift there's a place to enter notes for that shift.  <BR><BR>
