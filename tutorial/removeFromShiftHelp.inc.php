	<p> <strong>How to Remove a Volunteer From a Shift</strong>
	<p>  To begin, you must have already selected <strong>(edit this week)</strong> at the top of the calendar.
	<p> <B>Step 1:</B> Click on a calendar shift that has a volunteer working it already, like this:<BR><BR>
	<a href="tutorial/screenshots/removeFromShiftHelp_choose_shift.png" class="image" title="removeFromShiftHelp_choose_shift.png"
			horizontalalign="center" target="tutorial/screenshots/removeFromShiftHelp_choose_shift.png">
		 &nbsp&nbsp&nbsp&nbsp<img src="tutorial/screenshots/removeFromShiftHelp_choose_shift.png" width="10%" border="1px" align="center"></a>
	<br><br> (Each upcoming calendar shift will turn gray whenever the mouse passes over it.  Shifts for prior days on the calendar cannot
	be edited in this way.)
	<p> <B>Step 2:</B> This will give you a shift form that looks like this:<BR><BR>
	<a href="tutorial/screenshots/removeFromShiftHelp_shift_view.png" class="image" title="removeFromShiftHelp_shift_view.png"
			horizontalalign="center" target="tutorial/screenshots/removeFromShiftHelp_shift_view.png">
		 &nbsp&nbsp&nbsp&nbsp<img src="tutorial/screenshots/removeFromShiftHelp_shift_view.png" width="10%" border="1px" align="center"></a>
	<p> <B>Step 3:</B> Click on the <B>Remove Person/Create Vacancy</B> button beside the person you want to remove, like this:<BR><BR>
		<a href="tutorial/screenshots/removeFromShiftHelp_remove_shift.png" class="image" title="removeFromShiftHelp_remove_shift.png"
			horizontalalign="center" target="tutorial/screenshots/removeFromShiftHelp_remove_shift.png">
		 &nbsp&nbsp&nbsp&nbsp<img src="tutorial/screenshots/removeFromShiftHelp_remove_shift.png" width="10%" border="1px" align="center"></a>

<br><br>That person is no longer assigned to the shift and his/her slot becomes a <b>Vacancy</b>.<BR><BR>

	<p> <B>Step 4:</B>  You can return to the calendar or to any other function by selecting it on the navigation bar.