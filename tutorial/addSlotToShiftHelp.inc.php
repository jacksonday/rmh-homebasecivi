
	<p> <strong>How to Add or Remove a Slot from a Shift</strong>
	<p> To begin, you must have already selected <strong>(edit this week)</strong> at the top of the calendar:

	<p> <B>Step 1:</B> Click on a calendar shift that has a vacancy, like this:<BR><BR>
	<a href="tutorial/screenshots/addslotstep1.png" class="image" title="addslotstep1.png"
			horizontalalign="center" target="tutorial/screenshots/addslotstep1.png">
		 &nbsp&nbsp&nbsp&nbsp<img src="tutorial/screenshots/addslotstep1.png" width="10%" border="1px" align="center"></a>
	<br><br> (Each upcoming calendar shift will turn gray whenever the mouse passes over it.  Shifts for prior days on the calendar cannot
	be edited in this way.)
	<p> <B>Step 2:</B> This will give you a shift form that looks like this:<BR><BR>
	<a href="tutorial/screenshots/addslotstep2.png" class="image" title="addslotstep2.png"
			horizontalalign="center" target="tutorial/screenshots/addslotstep2.png">
		 &nbsp&nbsp&nbsp&nbsp<img src="tutorial/screenshots/addslotstep2.png" width="10%" border="1px" align="center"></a>
	<p> <B>Step 3:</B> Click on the <B>Add slot</B> button in the top row of the table, like this:<BR><BR>
		<a href="tutorial/screenshots/addslotstep3.png" class="image" title="addslotstep3.png"
			horizontalalign="center" target="tutorial/screenshots/addslotstep3.png">
		 &nbsp&nbsp&nbsp&nbsp<img src="tutorial/screenshots/addslotstep3.png" width="10%" border="1px" align="center"></a>

	<p> <B>Step 4:</B> A new vacant slot is added to the shift and appears as a new row. You can then assign
			a volunteer to this new slot.<BR><BR>
<a href="tutorial/screenshots/addslotstep4.png" class="image" title="addslotstep4.png"
			horizontalalign="center" target="tutorial/screenshots/addslotstep4.png">
		 &nbsp&nbsp&nbsp&nbsp<img src="tutorial/screenshots/addslotstep4.png" width="10%" border="1px" align="center"></a>

	<p> <B>Step 5:</B> To remove a vacant slot from a shift, click on the <b>Ignore Vacancy</b> button to the right of that slot, like this.<BR><BR>
<a href="tutorial/screenshots/addslotstep5.png" class="image" title="addslotstep5.png"
			horizontalalign="center" target="tutorial/screenshots/addslotstep5.png">
		 &nbsp&nbsp&nbsp&nbsp<img src="tutorial/screenshots/addslotstep5.png" width="10%" border="1px" align="center"></a>

	<br>Note: you cannot remove a slot that is not vacant without first removing the person from the slot.
	<p> <B>Step 6:</B> You can return to any other function by selecting it on the navigation bar.